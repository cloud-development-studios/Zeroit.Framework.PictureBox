namespace Zeroit.Framework.PictureBox
{

    /// <summary>
    /// Defines the states an <see cref="AnimatedPictureButton"/> can be in.
    /// </summary>
    public enum AnimatedButtonState
    /// <summary>
    /// Enum AnimatedButtonState
    /// </summary>
    {
        /// <summary>
        /// <see cref="AnimatedPictureButton.StartState"/> is shown
        /// (mouse is not hovering the button).
        /// </summary>
        /// <summary>
        /// The start
        /// </summary>
        Start,
        /// <summary>
        /// The start
        /// </summary>
        /// <summary>
        /// <see cref="AnimatedPictureButton.EndState"/> is shown
        /// (mouse is hovering the button).
        /// </summary>
        /// <summary>
        /// The end
        /// </summary>
        End,
        /// <summary>
        /// The end
        /// </summary>
        /// <summary>
        /// <see cref="AnimatedPictureButton.PushedState"/> is shown
        /// (mouse button is currently down on button).
        /// </summary>
        /// <summary>
        /// The pushed
        /// </summary>
        Pushed
        /// <summary>
        /// The pushed
        /// </summary>
    }
